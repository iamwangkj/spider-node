/**
 * 为了防止过快请求导致被服务器拒绝，使用延迟请求
 * @param {array} pList 元素为异步函数的数组
 * @param {number} delay 延迟的时间，单位为秒，默认延时3秒
 */
function delayRequest (fnList, delay = 3) {
  return new Promise((resolve, reject) => {
    const len = fnList.length
    let i = 0
    const res = []
    const timer = setInterval(async () => {
      if (i === len) {
        clearInterval(timer)
        resolve(res)
      } else {
        const fn = fnList[i]
        const resItem = await fn()
        res.push(resItem)
        ++i
      }
    }, delay * 1000)
  })
}

